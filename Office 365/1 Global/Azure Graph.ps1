# Azure Graph is using to connect to Microsoft Azure AD, or Entra ID, then run commands against objects in Azure AD (Users, Groups, etc)
# Install the module
Install-Module -Name Microsoft.Graph  

# Update the module
Update-Module -Name Microsoft.Graph 

# Import the module (should not usually be needed)
Import-Module -Name Microsoft.Graph

